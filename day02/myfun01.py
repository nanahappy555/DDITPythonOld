def add(a,b):
    return a+b

def minus(a,b):
    return a-b

def multiply(a,b):
    return a*b

def divide(a,b):
    return a/b

sum = add(1,6)
print("sum",sum)

result1 = minus(1,6)
print("minus",result1)

result2 = multiply(1,6)
